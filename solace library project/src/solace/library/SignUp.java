    private void btn_signupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_signupMouseClicked
        
        String name=txt_name.getText();
        String idno=txt_idno.getText();
        String address=txt_address.getText();
        String telno=txt_telNo.getText();
        String email=txt_email.getText();
        String username=txt_un.getText();
        String password=txt_pw.getText();
        String Repassword=txt_repw.getText();
        String adminpw=txt_adminpw.getText();
        String position=cmb_Position.getSelectedItem().toString();
        
        String sha1=encryption.SHA1(password);
        String sha512=encryption.SHA512(sha1);
        String md5=encryption.MD5(sha512);
        
        if((name.isEmpty() || idno.isEmpty() || address.isEmpty() || telno.isEmpty() || email.isEmpty() || username.isEmpty() || password.isEmpty() || Repassword.isEmpty() || adminpw.isEmpty()) ){
            
            JOptionPane.showMessageDialog(null,"Please complete the form corectly");
            System.out.println("00");
            
            
   
        }
        
        else if(!password.equals(Repassword)){
                JOptionPane.showMessageDialog(null,"password is not match");
                System.out.println("11");
                setColor(txt_repw);
            }
        
        else if(!adminpw.equals("Lib#solace123")){
            JOptionPane.showMessageDialog(null,"admin password is wrong");
                System.out.println("22");
                setColor(txt_adminpw);
            
        }
        
        
        
        
        else{        
            
            try {
                
                String sql="insert into users(IDNo,name,address,telno,email,username,password,position) values(?,?,?,?,?,?,?,?)";
                
                pst=con.prepareStatement(sql);
                
                pst.setString(1,idno);
                pst.setString(2,name);
                pst.setString(3,address);
                pst.setString(4,telno);
                pst.setString(5,email);
                pst.setString(6,username);
                pst.setString(7,md5);
                pst.setString(8,position);
                
                pst.execute();
                
                JOptionPane.showMessageDialog(null,"Signed up successfully");
                
                clear();
                
                Login ll = new Login();
                ll.setVisible(true);
                this.dispose();
                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
        }
        
        
        
         
    }
    
