       books_table.getColumnModel().getColumn(12).setPreferredWidth(70);
       
       books_table.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
            
            
            //burrow book table
            String selectbbk = "select ISBN,Book_Name,Member_ID,Name,date,return_date from burrow_book";
            pst = con.prepareStatement(selectbbk);
            rs=pst.executeQuery();
            
